#include <iostream>
#include <vector>
#include <string>

using namespace std;

int main()
{ // Open curly brace
    cout << "Hello World!" << endl;
    cout << "--------------" << endl;
    cout << "Hello Triangle!" << endl; // C-out, open double angle brackets(stream insertion operator), open double quote, H-e-l-l-o, space, T-r-i-a-n-g-l-e,  exclamation mark,close parenthesis, close double quote, end-l, semicolon
    cout << "      /|" << endl;
    cout << "     / |" << endl;
    cout << "    /  |" << endl;
    cout << "   /   |" << endl;
    cout << "  /    |" << endl;
    cout << " /__ __|" << endl;
    
    return 0; // Return zero, semicolon
}  // Close curly brace 